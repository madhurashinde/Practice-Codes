#include<stdio.h>

int main()

{
	
	char ch;
	
	printf("Enter the character\n");
	
	scanf("%c",&ch);
	
	switch(ch)
	
	{
		
		case'a':

		
		case'A':
		
		
		case'o':
		
		
		case'O':
		
		
		case'e':
		
		
		case'E':
		
		
		case'i':

		
		case'I':
		
		
		case'u':
		
		
		case'U':
		
		
		printf("entered character %c is a vowel\n",ch);
		
		break;
		
		default:
		
		printf("entered character %c is a consonant\n",ch);
	
	} 

}
